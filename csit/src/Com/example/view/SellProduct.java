    private void jTextField_productidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField_productidActionPerformed
        // TODO add your handling code here:
        String id=jTextField_productid.getText();
        if(!id.equals("")){
            int pid=Integer.parseInt(id);
            ProductDAO pDAO=new ProductDAO();
            ArrayList<Product> list=pDAO.fetchDataWithId(pid);
            if(list.size()>0){
                model.addRow(new Object[]{list.get(0).getId(),list.get(0).getName(),list.get(0).getPrice(),"1",list.get(0).getPrice()});
                computeGrandTotal();
            }
        }
    }//GEN-LAST:event_jTextField_productidActionPerformed

    private void jButton_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeActionPerformed
        // TODO add your handling code here:
        int row=jTable_sellproduct.getSelectedRow();
        model.removeRow(row);
        computeGrandTotal();
    }//GEN-LAST:event_jButton_removeActionPerformed
    public void printBill(){
        String str="Namaste Super Market\nSinamangal,Kathmandu\n\n\n";
        
        int rowCount=jTable_sellproduct.getRowCount();
        for(int i=0;i<rowCount;i++){
            str+=model.getValueAt(i, 0)+" ";
            str+=model.getValueAt(i, 1)+" ";
            str+=model.getValueAt(i, 2)+" ";
            str+=model.getValueAt(i, 3)+" ";
            str+=model.getValueAt(i, 4)+"\n";
        }
        str+="Grand Total: "+jLabel_grandtotal.getText();
        str+="\nThank You for visiting us!!!!!!!!!!!!";
        
        BillPrint ob=new BillPrint();
        ob.jTextArea_Bill.setLineWrap(true);
        ob.jTextArea_Bill.setText(str);
        try{
            ob.jTextArea_Bill.print();
            
        }catch(Exception ex){
            System.out.println(ex);
        }
        
           
        
    }
    public void clearTableRow(){
        int count=jTable_sellproduct.getRowCount();
        for(int i=0;i<count;i++){
            model.removeRow(0);
        }
        jTextField_productid.setText("");
    }
        
    private void jButton_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_printActionPerformed
        // TODO add your handling code here:
        ArrayList<Sales> list=new ArrayList();
        int count=jTable_sellproduct.getRowCount();
        for(int i=0;i<count;i++)
        {
            int pid=Integer.parseInt(model.getValueAt(i,0).toString());
            int sid=TrackUser.uid;
            double price=Double.parseDouble(model.getValueAt(i, 2).toString());
            int qty=Integer.parseInt(model.getValueAt(i, 3).toString());
            Date d=new Date();
            //create model class sales object and set these values
            Sales ob= new Sales();
            ob.setPid(pid);
            ob.setSid(sid);
            ob.setPrice(price);
            ob.setQty(qty);
            ob.setDos(d);
            //add this object to list
            list.add(ob);
                  
    }//GEN-LAST:event_jButton_printActionPerformed
//hand over this list to controller salesDAO
        SalesDAO sDAO= new SalesDAO();
            sDAO.insertData(list);
            printBill();
            clearTableRow();
    }

    private void computeGrandTotal(){
        int rowCount=jTable_sellproduct.getRowCount();
        double gTotal=0;
        for(int i=0;i<rowCount;i++){
            double total=Double.parseDouble(jTable_sellproduct.getValueAt(i,4).toString());
            gTotal+=total;
        }
        jLabel_grandtotal.setText(gTotal+"");
    }
